/**
 * Prints HTML with meta information for the categories, tags and comments.
 */
function digitalnoir_entry_footer() {
	// Hide category and tag text for pages.
	if ( 'post' == get_post_type() ) {
		/* translators: used between list items, there is a space after the comma */
		$categories_list = get_the_category_list( __( ', ', 'digitalnoir' ) );
		if ( $categories_list && digitalnoir_categorized_blog() ) {
			printf( '<span class="cat-links">' . __( 'Posted in %1$s', 'digitalnoir' ) . '</span>', $categories_list );
		}

	}

	edit_post_link( __( 'Edit', 'digitalnoir' ), '<span class="edit-link">', '</span>' );
}
